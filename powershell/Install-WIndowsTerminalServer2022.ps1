# How to install Windows Terminal on Windows Server 2022

Invoke-WebRequest -Uri https://github.com/microsoft/terminal/releases/download/v1.7.1091.0/Microsoft.WindowsTerminal\_1.7.1091.0\_8wekyb3d8bbwe.msixbundle -outfile Microsoft.WindowsTerminal\_1.7.1091.0\_8wekyb3d8bbwe.msixbundle
Add-AppxPackage -Path .\\Microsoft.WindowsTerminal\_1.7.1091.0\_8wekyb3d8bbwe.

